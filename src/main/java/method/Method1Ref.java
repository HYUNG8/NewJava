package method;

public class Method1Ref {

	public static void main(String[] args) {
		
		
		// 계산1 
		
		int a = 1;
		int b = 2;
		
		System.out.println(a + "+" + b + " 연산 수행");
		
		int result = a + b;
		
		System.out.println("결과값: " + result);
		
		// 계산2
		
		int x = 10;
		int y = 20;
		
		System.out.println(x + "+" + y + " 연산 수행");
		
		int result2 = x + y;
		
		System.out.println("연산결과: " + result2);
	}

}
